char	*ft_strdup(char *src)
{
	int i;
	int j;
	char *str;
	len = 0;
	while(src[j])
	{
		j++;
	}
	str = ((char*)malloc(sizeof(*str)) * (j + 1));
	while(i < len)
	{
		str[i] = src[i];
		i++;
	}
	return str;
}

